/**
 * gen-font-icons.mjs
 *
 * Reads all SVG files from ./icons (except Icons.svg) and generates
 * src/lib/icons/font-icons-data.ts — a static TypeScript module embedding every
 * icon's viewBox + innerHTML so they can be used as a custom icon.
 *
 * Run:  node scripts/gen-font-icons.mjs
 *       (or: npm run gen-icons)
 *
 * To generate svg files:
 * Open Icons.otf in FontForge
 * Element -> Font Info -> General -> Set Descent to 0
 * File -> Execute Script -> select FF
 *
 * SelectWorthOutputting(); foreach Export("path/to/frontend/scripts/icons/%e.svg"); endloop;
 *
 */

import {readdirSync, readFileSync, writeFileSync} from "node:fs";
import {basename, dirname, resolve} from "node:path";
import {fileURLToPath} from "node:url";

const __dir = dirname(fileURLToPath(import.meta.url));
const iconsDir = resolve(__dir, "./data");
const outFile = resolve(__dir, "../../src/components/icons/font-icons-data.ts");

const files = readdirSync(iconsDir)
    .filter((f) => f.endsWith(".svg"))
    .sort();

/**
 * From an SVG string, extract:
 * - viewBox attribute value
 * - the entire inner HTML (everything between the opening and closing <svg> tag)
 */
function parseSvg(content) {
    const viewBoxMatch = content.match(/viewBox="([^"]+)"/);
    if (!viewBoxMatch) return null;
    const viewBox = viewBoxMatch[1];

    // Everything between the first '<path>' and ending '</svg>'
    const open = content.indexOf("<path ");
    const close = content.lastIndexOf("</svg>");
    if (open === -1 || close === -1) return null;
    // clean into one line for the final file
    const inner = content.slice(open, close).trim().replaceAll("\r\n", "\n").replaceAll("\n", " ");

    return { viewBox, inner };
}

const byName = new Map(); // codepoint → { viewBox, inner }

for (const file of files) {
    const encoding = basename(file, ".svg");
    const content = readFileSync(resolve(iconsDir, file), "utf8");
    const parsed = parseSvg(content);
    if (!parsed) {
        console.warn(`  skip ${file}: could not parse`);
        continue;
    }
    // fontforge exports the .notdef glyph in position FFFF + 1 when using encodings.
    // in unicode, it exports it to xxxx, but also fails to export FFFF, which we need
    // so we're left with this, where we have to export %e but make a special symbol for the .notdef glyph
    const codepoint = encoding === "65536" ? "xxxx" : parseInt(encoding, 10).toString(16).toUpperCase().padStart(4, "0");
    byName.set(codepoint, parsed);
}

console.log(`Parsed ${byName.size} icons.`);

// Build TypeScript output
const lines = [
    `// AUTO-GENERATED by scripts/gen-font-icons.mjs — do not edit.`,
    `// Re-run: npm run gen-icons`,
    ``,
    `export interface FontIconEntry { viewBox: string; c: string }`,
    ``,
    `/** All font icons keyed by Unicode code point (filename without .svg). */`,
    `export const GLYPH_ICONS: Record<string, FontIconEntry> = {`,
];

for (const [name, { viewBox, inner }] of [...byName.entries()].sort()) {
    lines.push(
        `  ${JSON.stringify(name)}: { viewBox: ${JSON.stringify(viewBox)}, c: ${JSON.stringify(inner)} },`
    );
}
lines.push(`};`, ``);

writeFileSync(outFile, lines.join("\n"), "utf8");
console.log(`Written → ${outFile}`);

